public class ExemploEstruturaCondicional {
    public static void main(String[] args){
	int numero = 102;
	// se (numero for maior que 0) entao
	if (numero > 0) {
	   System.out.println("Número positivo");
	} else if (numero == 0){
	   System.out.println("Número 0");
	} else {
	   System.out.println("Número negativo");
	} 
	// if é se
	// else if é senão se
	// else é senão
	// Operadores relacionais
	// Igual		==
	// Menor		<
	// Menor ou igual	<=
	// Maior		>
	// Maior ou igual	>=
	// Diferente		!=
    }
}