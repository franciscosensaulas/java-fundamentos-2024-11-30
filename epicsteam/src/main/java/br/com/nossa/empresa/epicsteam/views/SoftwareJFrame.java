    private void jButtonCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastrarActionPerformed
        String nome = jTextFieldNome.getText();
        String url = jTextFieldUrl.getText();
        CategoriaBean categoria = (CategoriaBean) jComboBoxCategoria.getSelectedItem();
        
        var software = new SoftwareBean();
        software.setNome(nome);
        software.setUrl(url);
        software.setCategoria(categoria);
        
        var softwareDao = new SoftwareDao();
        softwareDao.cadastrar(software);
        dispose();
    }//GEN-LAST:event_jButtonCadastrarActionPerformed

    private void carregarCategoriasNoComboBox(){
        // Consultando todas as categorias na tabela de categorias, 
        // que retonra uma lista de categorias
        var categorias = new CategoriaDao().obterTodas();
        // Pegando o modelo do combobox
        var modelo = (DefaultComboBoxModel) jComboBoxCategoria.getModel();
        
        // Limpar os itens do jComboBox
        modelo.removeAllElements();
        // Percorrendo cada uma das categorias, adicionando no modelo, 
        // para apresentar para o usuÃ¡rio poder escolher a categoria
        categorias.forEach(categoria -> modelo.addElement(categoria));
    }
    
